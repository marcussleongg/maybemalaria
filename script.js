function deg2rad(deg) {
    return deg * (Math.PI/180);
}

function calculateDist(inputArray, historicalArray) {
    //haversine formula for distance approximation
    var lat1 = inputArray[0];
    var lon1 = inputArray[1];
    var lat2 = historicalArray[0];
    var lon2 = historicalArray[1];
    var R = 6371; // Radius of the earth in km
    var dLat = deg2rad(lat2-lat1);  // deg2rad above
    var dLon = deg2rad(lon2-lon1); 
    var a = Math.sin(dLat/2) * Math.sin(dLat/2) + Math.cos(deg2rad(lat1)) * Math.cos(deg2rad(lat2)) * Math.sin(dLon/2) * Math.sin(dLon/2); 
    var c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a)); 
    var dist = R * c; // Distance in km
    return dist;
}

